//! Shared helpers for collecting Tamanu server identity and host facts.
//!
//! Used by `canopy register`, `alertd`, and `doctor`. Kept here so each
//! subcommand pulls from one place rather than reaching into a sibling's module.

use std::path::{Path, PathBuf};

use miette::{IntoDiagnostic, Result, WrapErr};
use tracing::{debug, info, warn};
use uuid::Uuid;

/// Standard on-disk location for the Tamanu device key PEM.
///
/// - Linux: `/etc/tamanu/device-key.pem`
/// - Windows: `C:\Tamanu\device-key.pem`
/// - Other platforms: same as Linux.
///
/// This is the source of truth for the device key. The Tamanu DB's
/// `local_system_facts.deviceKey` row is a legacy fallback retained so older
/// installs can be migrated forward; new keys are generated by
/// [`get_or_create_device_key`] and written here.
pub fn standard_device_key_path() -> PathBuf {
	if cfg!(windows) {
		PathBuf::from(r"C:\Tamanu\device-key.pem")
	} else {
		PathBuf::from("/etc/tamanu/device-key.pem")
	}
}

/// Standard on-disk location for the Tamanu meta-server ID.
///
/// - Linux: `/etc/tamanu/server-id`
/// - Windows: `C:\Tamanu\server-id`
/// - Other platforms: same as Linux.
///
/// Like the device key, this is the preferred source; the
/// `local_system_facts.metaServerId` row is the legacy fallback.
pub fn standard_server_id_path() -> PathBuf {
	if cfg!(windows) {
		PathBuf::from(r"C:\Tamanu\server-id")
	} else {
		PathBuf::from("/etc/tamanu/server-id")
	}
}

/// Standard on-disk location for cached canopy tags.
///
/// Sits alongside `server-id` and `device-key.pem` so all per-host canopy
/// identity / metadata files share one directory. Refreshed every time
/// `bestool tamanu tags` runs successfully online; consulted (and trusted as
/// the source of truth) when canopy is unreachable.
pub fn standard_tags_path() -> PathBuf {
	if cfg!(windows) {
		PathBuf::from(r"C:\Tamanu\tags.json")
	} else {
		PathBuf::from("/etc/tamanu/tags.json")
	}
}

/// Resolve the `metaServerId` for this Tamanu server.
///
/// Resolution order:
/// 1. Read [`standard_server_id_path`] if present. This is the only step that
///    works without a DB connection; on already-provisioned hosts it always
///    succeeds, so callers like the doctor daemon can report status to canopy
///    even when postgres is down (which is precisely when canopy most needs
///    to hear from us).
/// 2. If `client` is `Some`, read the legacy `local_system_facts.metaServerId`
///    row; best-effort copy to the file path so subsequent runs don't need
///    the DB.
/// 3. If `client` is `Some`, generate a fresh UUIDv4. Persist to the file
///    path; if the file write fails, fall back to inserting into
///    `local_system_facts` so the new ID isn't lost across runs.
///
/// Returns an error when `client` is `None` *and* the file isn't present —
/// brand-new hosts have to reach the DB at least once to mint an ID.
pub async fn get_or_create_server_id(client: Option<&tokio_postgres::Client>) -> Result<String> {
	get_or_create_server_id_at(&standard_server_id_path(), client).await
}

/// Test-shimmed core of [`get_or_create_server_id`] — same contract, with
/// the file path injected so unit tests can drive it without touching
/// `/etc/tamanu`.
async fn get_or_create_server_id_at(
	path: &Path,
	client: Option<&tokio_postgres::Client>,
) -> Result<String> {
	if let Some(id) = read_server_id_file(path) {
		return Ok(id);
	}

	let Some(client) = client else {
		miette::bail!(
			"no server-id file at {} and no DB connection to mint one from",
			path.display()
		);
	};

	if let Some(id) = query_server_id_row(client).await? {
		match write_server_id_file(path, &id) {
			Ok(()) => info!(
				path = %path.display(),
				"copied metaServerId from Tamanu DB to standard path"
			),
			Err(err) => debug!(
				path = %path.display(),
				%err,
				"could not copy metaServerId to standard path; will retry next run"
			),
		}
		return Ok(id);
	}

	let id = Uuid::new_v4().to_string();
	info!(server_id = %id, "generating new metaServerId");

	match write_server_id_file(path, &id) {
		Ok(()) => Ok(id),
		Err(err) => {
			debug!(
				path = %path.display(),
				%err,
				"could not persist new metaServerId to file; falling back to DB"
			);
			client
				.execute(
					"INSERT INTO local_system_facts (key, value) VALUES ('metaServerId', $1)",
					&[&id],
				)
				.await
				.into_diagnostic()?;
			Ok(id)
		}
	}
}

/// Read just the `metaServerId` row from the Tamanu DB, without creating one.
async fn query_server_id_row(client: &tokio_postgres::Client) -> Result<Option<String>> {
	let row = client
		.query_opt(
			"SELECT value FROM local_system_facts WHERE key = 'metaServerId'",
			&[],
		)
		.await
		.into_diagnostic()?;

	let Some(row) = row else {
		return Ok(None);
	};
	let id: String = row.try_get(0).into_diagnostic()?;
	debug!(server_id = %id, "found existing metaServerId in DB");
	Ok(Some(id))
}

fn read_server_id_file(path: &Path) -> Option<String> {
	match std::fs::read_to_string(path) {
		Ok(s) => {
			let trimmed = s.trim();
			if trimmed.is_empty() {
				warn!(path = %path.display(), "server-id file is empty; ignoring");
				return None;
			}
			if Uuid::parse_str(trimmed).is_err() {
				warn!(
					path = %path.display(),
					"server-id file does not contain a UUID; ignoring",
				);
				return None;
			}
			debug!(path = %path.display(), server_id = trimmed, "loaded metaServerId from standard path");
			Some(trimmed.to_string())
		}
		Err(err) if err.kind() == std::io::ErrorKind::NotFound => None,
		Err(err) => {
			debug!(path = %path.display(), %err, "could not read server-id file");
			None
		}
	}
}

fn write_server_id_file(path: &Path, id: &str) -> std::io::Result<()> {
	use std::io::Write as _;

	if let Some(parent) = path.parent()
		&& !parent.exists()
	{
		return Err(std::io::Error::new(
			std::io::ErrorKind::NotFound,
			"parent directory does not exist",
		));
	}

	let tmp = path.with_extension("tmp");
	{
		let mut f = std::fs::OpenOptions::new()
			.write(true)
			.create_new(true)
			.open(&tmp)?;
		#[cfg(unix)]
		{
			use std::os::unix::fs::PermissionsExt as _;
			let perms = std::fs::Permissions::from_mode(0o644);
			f.set_permissions(perms)?;
		}
		f.write_all(id.as_bytes())?;
		f.write_all(b"\n")?;
		f.sync_all()?;
	}
	std::fs::rename(&tmp, path)
}

/// Resolve the device key from a file, creating one if missing.
///
/// Reads the PEM at `path`; if absent, generates a fresh P-256 private key and
/// writes it there. Unlike the DB-backed resolvers this never touches the
/// Tamanu database, so `canopy register` can provision the machine's mTLS
/// identity without a database connection.
///
/// Generating a key here is safe in the operator-first canopy flow: the
/// operator creates the server record in canopy before running register, and
/// register publishes the machine's public key to it over mTLS, so a freshly
/// minted key is bound rather than stranded.
#[cfg(feature = "device-key")]
pub fn get_or_create_device_key_file(path: &Path) -> Result<String> {
	if let Some(pem) = read_device_key_file(path) {
		return Ok(pem);
	}

	let pem = generate_device_key_pem()?;
	info!(path = %path.display(), "generating new device key");
	write_device_key_file(path, &pem)
		.into_diagnostic()
		.wrap_err("persisting generated device key")?;
	Ok(pem)
}

#[cfg(feature = "device-key")]
fn generate_device_key_pem() -> Result<String> {
	use miette::miette;
	use p256::{
		SecretKey,
		elliptic_curve::rand_core::OsRng,
		pkcs8::{EncodePrivateKey as _, LineEnding},
	};

	let key = SecretKey::random(&mut OsRng);
	let pem = key
		.to_pkcs8_pem(LineEnding::LF)
		.map_err(|e| miette!("failed to encode generated deviceKey: {e}"))?;
	Ok(pem.to_string())
}

/// Best-effort load of the Tamanu deviceKey PEM.
///
/// Tries the standard file path first ([`standard_device_key_path`]). If
/// that isn't readable, falls back to the legacy
/// `local_system_facts.deviceKey` row via the caller-supplied closure (which
/// receives the chance to use a pre-existing DB client). When the fallback
/// succeeds and the standard path doesn't already exist, attempts a
/// best-effort copy so subsequent runs don't need the DB.
///
/// Returns `None` if neither source yields a key. Logging is the only
/// signal: callers without a device key continue to work (canopy tailscale
/// path is still available).
pub async fn fetch_device_key_with<F, Fut>(db_fetch: F) -> Option<String>
where
	F: FnOnce() -> Fut,
	Fut: std::future::Future<Output = Option<String>>,
{
	let path = standard_device_key_path();

	if let Some(pem) = read_device_key_file(&path) {
		return Some(pem);
	}

	let pem = db_fetch().await?;

	if !path.exists() {
		match write_device_key_file(&path, &pem) {
			Ok(()) => info!(
				path = %path.display(),
				"copied deviceKey from Tamanu DB to standard path"
			),
			Err(err) => debug!(
				path = %path.display(),
				%err,
				"could not copy deviceKey to standard path; will retry next run"
			),
		}
	}

	Some(pem)
}

/// Query the central server's `settings` table for `features.patientPortal`.
///
/// Tamanu mounts the patient portal API conditionally on this flag (see
/// `packages/central-server/app/createApi.js`), so a `true` value here is the
/// authoritative "this deployment runs the portal" signal — independent of
/// the ansible-side `PatientPortalFQDN` tag or the unused `patientPortal.portalUrl`
/// config field.
///
/// Settings are stored as one row per dotted leaf path, with the value column
/// as `JSONB`. The default if the row is absent is `false` (per the global
/// schema in `packages/settings/src/schema/global.ts`), so missing rows
/// resolve to `Some(false)`.
///
/// Returns `None` only when the query itself failed — that's the
/// "DB unreachable" / "transient SQL error" case, kept distinct from
/// `Some(false)` so callers can emit an Unknown expectation rather than
/// silently treating outage as opt-out.
pub async fn query_patient_portal_enabled(client: &tokio_postgres::Client) -> Option<bool> {
	match client
		.query_opt(
			"SELECT value FROM settings WHERE key = 'features.patientPortal' LIMIT 1",
			&[],
		)
		.await
	{
		Ok(Some(row)) => Some(
			row.try_get::<_, serde_json::Value>(0)
				.ok()
				.and_then(|v| v.as_bool())
				.unwrap_or(false),
		),
		Ok(None) => Some(false),
		Err(err) => {
			debug!(%err, "could not query features.patientPortal setting");
			None
		}
	}
}

/// Query `local_system_facts.deviceKey` on an existing Tamanu DB client.
///
/// Logs at warn/info on errors and missing rows, returning `None`. Suitable
/// to pass as the closure to [`fetch_device_key_with`].
pub async fn query_device_key_row(client: &tokio_postgres::Client) -> Option<String> {
	match client
		.query_opt(
			"SELECT value FROM local_system_facts WHERE key = 'deviceKey'",
			&[],
		)
		.await
	{
		Ok(Some(row)) => match row.try_get::<_, String>(0) {
			Ok(pem) => {
				info!("loaded deviceKey from Tamanu DB for canopy targets");
				Some(pem)
			}
			Err(err) => {
				warn!("deviceKey row not a string: {err}");
				None
			}
		},
		Ok(None) => {
			info!("no deviceKey in local_system_facts; canopy targets unavailable");
			None
		}
		Err(err) => {
			warn!("failed to query deviceKey: {err}");
			None
		}
	}
}

fn read_device_key_file(path: &Path) -> Option<String> {
	match std::fs::read_to_string(path) {
		Ok(s) if !s.trim().is_empty() => {
			debug!(path = %path.display(), "loaded deviceKey from standard path");
			Some(s)
		}
		Ok(_) => {
			warn!(path = %path.display(), "deviceKey file is empty; ignoring");
			None
		}
		Err(err) if err.kind() == std::io::ErrorKind::NotFound => None,
		Err(err) => {
			debug!(path = %path.display(), %err, "could not read deviceKey file");
			None
		}
	}
}

fn write_device_key_file(path: &Path, pem: &str) -> std::io::Result<()> {
	use std::io::Write as _;

	if let Some(parent) = path.parent()
		&& !parent.exists()
	{
		return Err(std::io::Error::new(
			std::io::ErrorKind::NotFound,
			"parent directory does not exist",
		));
	}

	// Write to a sibling temp file then rename, so a partial write never
	// leaves a half-readable PEM at the target path.
	let tmp = path.with_extension("pem.tmp");
	{
		let mut opts = std::fs::OpenOptions::new();
		opts.write(true).create_new(true);
		#[cfg(windows)]
		{
			use std::os::windows::fs::OpenOptionsExt as _;
			// FILE_ATTRIBUTE_HIDDEN — tucks the key out of casual Explorer view.
			// The attribute is set on creation so it applies before any bytes
			// land, and survives the rename below.
			const FILE_ATTRIBUTE_HIDDEN: u32 = 0x0000_0002;
			opts.attributes(FILE_ATTRIBUTE_HIDDEN);
		}
		let mut f = opts.open(&tmp)?;
		#[cfg(unix)]
		{
			use std::os::unix::fs::PermissionsExt as _;
			let perms = std::fs::Permissions::from_mode(0o600);
			f.set_permissions(perms)?;
		}
		f.write_all(pem.as_bytes())?;
		f.sync_all()?;
	}
	std::fs::rename(&tmp, path)
}

/// Read the tailscale Self node's first IP and DNS name, if tailscale is
/// installed and responding to `status --json`.
pub async fn get_tailscale_info() -> (Option<String>, Option<String>) {
	let output = match tokio::process::Command::new("tailscale")
		.arg("status")
		.arg("--json")
		.output()
		.await
	{
		Ok(output) if output.status.success() => output,
		Ok(output) => {
			debug!(
				status = %output.status,
				"tailscale status command failed"
			);
			return (None, None);
		}
		Err(e) => {
			debug!(error = %e, "tailscale not available");
			return (None, None);
		}
	};

	let parsed: serde_json::Value = match serde_json::from_slice(&output.stdout) {
		Ok(v) => v,
		Err(e) => {
			warn!(error = %e, "failed to parse tailscale status JSON");
			return (None, None);
		}
	};

	let self_node = &parsed["Self"];
	let ip = self_node["TailscaleIPs"]
		.as_array()
		.and_then(|ips| ips.first())
		.and_then(|ip| ip.as_str())
		.map(String::from);
	let name = self_node["DNSName"]
		.as_str()
		.map(|s| s.trim_end_matches('.').to_string());

	(ip, name)
}

/// Detect the host's installed Node.js version by running `node --version`.
///
/// Returns `None` if node isn't on `PATH` or the command fails. The leading
/// `v` that node prints (e.g. `v20.11.0`) is stripped, so the value is a bare
/// version string.
pub async fn detect_node_version() -> Option<String> {
	let output = tokio::process::Command::new("node")
		.arg("--version")
		.output()
		.await
		.ok()?;
	if !output.status.success() {
		debug!(status = %output.status, "node --version failed");
		return None;
	}

	parse_node_version(&String::from_utf8_lossy(&output.stdout))
}

/// Parse the output of `node --version` (e.g. `v20.11.0\n`) into a bare version
/// string. Returns `None` for empty/whitespace-only input.
fn parse_node_version(raw: &str) -> Option<String> {
	let version = raw.trim().trim_start_matches('v');
	if version.is_empty() {
		None
	} else {
		Some(version.to_string())
	}
}

/// Linux: read `systemd-detect-virt`'s output. Returns `None` if the command
/// is unavailable. The string is whatever systemd reports (e.g. `kvm`, `lxc`,
/// `none` for bare metal).
pub async fn detect_virtualisation() -> Option<String> {
	let output = tokio::process::Command::new("systemd-detect-virt")
		.output()
		.await
		.ok()?;

	let stdout = String::from_utf8_lossy(&output.stdout).trim().to_string();
	if stdout.is_empty() {
		return None;
	}

	Some(stdout)
}

